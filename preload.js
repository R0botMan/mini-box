const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('api', {
  login:      () => ipcRenderer.invoke('login'),
  isAuthed:   () => ipcRenderer.invoke('isAuthed'),
  getCurrent: () => ipcRenderer.invoke('getCurrent'),
  playPause:  () => ipcRenderer.invoke('playPause'),
  next:       () => ipcRenderer.invoke('next'),
  prev:       () => ipcRenderer.invoke('prev'),
  logout:     () => ipcRenderer.invoke('logout'),
  onAuthed:   (cb) => ipcRenderer.on('authed', cb),
  onLoggedOut:(cb) => ipcRenderer.on('loggedOut', cb),
  seek: (ms) => ipcRenderer.invoke('seek', ms),
  getVolume: () => ipcRenderer.invoke('getVolume'),
  setVolume: (pct) => ipcRenderer.invoke('setVolume', pct),
  getAccessToken: () => ipcRenderer.invoke('getAccessToken'),
  transferPlayback: (deviceId, play = true) => ipcRenderer.invoke('transferPlayback', deviceId, play),
  getDevices: () => ipcRenderer.invoke('getDevices'),
  growForOptions:  (px) => ipcRenderer.invoke('growForOptions', px),
  shrinkForOptions:(px) => ipcRenderer.invoke('shrinkAfterOptions', px),
  openSpotifyAccount: () => ipcRenderer.invoke('openSpotifyAccount'),
  closeApp: () => ipcRenderer.invoke('closeApp'),
  restartApp: () => ipcRenderer.invoke('restartApp'),
  getQueue: () => ipcRenderer.invoke('getQueue'),
  toggleQueue: () => ipcRenderer.invoke('toggleQueue'),
  toggleMore: () => ipcRenderer.invoke('toggleMore'),
  toggleSettings: () => ipcRenderer.invoke('toggleSettings'),
  onQueueOpened: (cb) => ipcRenderer.on('queueOpened', cb),
  onQueueClosed: (cb) => ipcRenderer.on('queueClosed', cb),
  onMoreOpened: (cb) => ipcRenderer.on('moreOpened', cb),
  onMoreClosed: (cb) => ipcRenderer.on('moreClosed', cb),
  onSettingsOpened: (cb) => ipcRenderer.on('settingsOpened', cb),
  onSettingsClosed: (cb) => ipcRenderer.on('settingsClosed', cb),
  getAppRuntime: () => ipcRenderer.invoke('getAppRuntime'),
  setTheme: (theme) => ipcRenderer.invoke('setTheme', theme),
  getTheme: () => ipcRenderer.invoke('getTheme'),
  onThemeChanged: (cb) => ipcRenderer.on('themeChanged', cb),
  getVersion: () => ipcRenderer.invoke('getVersion'),
  getBuildInfo: () => ipcRenderer.invoke('getBuildInfo'),
  checkForUpdates: () => ipcRenderer.invoke('checkForUpdates'),
  getUpdateStatus: () => ipcRenderer.invoke('getUpdateStatus'),
  installUpdate: () => ipcRenderer.invoke('installUpdate'),
  onUpdateStatusChanged: (cb) => ipcRenderer.on('updateStatusChanged', cb),
  forceRefresh: () => ipcRenderer.invoke('forceRefresh'),
  onForceRefresh: (cb) => ipcRenderer.on('forceRefresh', cb),
  getDebugLogPath: () => ipcRenderer.invoke('getDebugLogPath'),
  openDebugLog: () => ipcRenderer.invoke('openDebugLog'),
  rendererLog: (category, message) => ipcRenderer.invoke('rendererLog', category, message),
  clearCache: () => ipcRenderer.invoke('clearCache'),
  closePatch: () => ipcRenderer.invoke('closePatch'),
  openPatchWindow: () => ipcRenderer.invoke('openPatchWindow'),
});
